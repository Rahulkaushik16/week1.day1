package week1.day1.assignments;

public class PositiveOrNegativeNumber {

	public static void main(String[] args) {
		int num = 35; // Initialize a number, say, int number= 35

		if (num > 0) {

			System.out.println("The Number " + num + " is Positive Number"); //If a number is positive, print "The number is positive"
		}

		else if (num < 0) {

			System.out.println("The Number " + num + " is Negative Number"); //If a number is negative, print "The number is negative"
		}

		else {

			System.out.println("The Number " + num + " is neither positive nor negative"); //If it nether negative nor positive, print as "The number is neither positive nor negative"
		}

	}

}
